O comando [ ! -d "example_folder" ] verifica se o diretório mencionado não existe

-d verifica se o caminho é um diretório

! inverte a condição, para saber se o diretório não existe 


Caso o diretório não exista então irá ser criado por causa do comando "mkdir"

se o diretório existir então será feito o "example_folder_02"

